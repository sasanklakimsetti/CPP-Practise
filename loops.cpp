#include<iostream>
using namespace std;
int main()
{
    //for loop
    for(int i=0;i<10;i++)
    {
        cout<<i<<endl;
    }
    int a;
    //while loop
    while(a>0)
    {
        cout<<a<<" ";
        a--;
    }
    //do-while loop
    do
    {
        cout<<a<<" ";
    } while (a>0);
    return 0;
}